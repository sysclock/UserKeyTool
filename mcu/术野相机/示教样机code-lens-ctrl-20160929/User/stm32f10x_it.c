/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
u16 nTime;
u16 time_count=0;

extern unsigned char Key2_flag;
extern unsigned int Key2_time_count;
extern unsigned char Key2_num;

extern unsigned char Key1_flag;
extern unsigned int Key1_time_count;
void SysTick_Handler(void)
{	 
	SysTick_ITConfig(DISABLE);   //关闭中断
		if(nTime)
		nTime--; 
		
	if(zoomirq_flag)
	{
		zoomirq_time++;
	}
	
	if(Key2_flag) Key2_time_count++;
	if(Key1_flag) Key1_time_count++;
	time_count++;
	if(time_count>500)
	{
		if(work_led_flag)
		{
			GPIO_SetBits(GPIOA, GPIO_Pin_8);
			work_led_flag=0;
		}
		else
		{
			GPIO_ResetBits(GPIOA, GPIO_Pin_8);
			work_led_flag=1;
		}
		time_count=0;
	}
	SysTick_ITConfig(ENABLE);   //关闭中断
}

